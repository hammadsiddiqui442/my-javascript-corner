console.log("Hammad")
// OUTPUT:
// Hammad


console.log("Hammad\nHammad")
// OUTPUT:
// Hammad
// Hammad


console.log("Name = Hammad\nAge = 28\nCity = Karachi\nCountry = Pakistan")
// OUTPUT:
// Name = Hammad
// Age = 28
// City = Karachi
// Country = Pakistan


console.log("Hammad Hammad")
// OUTPUT:
// Hammad Hammad


console.log("Hammad\tHammad")
// OUTPUT:
// Hammad  Hammad


console.log("Hammad/tHammad")
// OUTPUT:
// Hammad/tHammad


console.log("My name is\t'Hammad'")
// OUTPUT:
// My name is      'Hammad'


console.log("My name is\t \"Hammad\" ")
// OUTPUT:
// My name is       "Hammad"


console.log("My name is\t \"Hammad\" \\ ")
// OUTPUT:
// My name is       "Hammad" \


console.log('My name is\t\'Hammad\'')
// OUTPUT:
// My name is      'Hammad'


console.log("Hammad" + "Hammad" + "Hammad") // Concatenation means plus strings
// OUTPUT:
// HammadHammadHammad

